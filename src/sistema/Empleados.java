    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        conexion conectar=new conexion();
        String sql;
        //insertar datos en la base de datos
        //se prepara la sentencia para insertar datos
        
        sql="INSERT INTO EMPLEADO(DNI_E,NOMBRE_E,APELLIDOS_E,TELEFONO_E,DIRECCION_E)";
        sql+="VALUES(?,?,?,?,?)";
        int DNI_E=Integer.parseInt(txtDNI_E.getText());
        String NOMBRE_E=txtNOMBRES_E.getText();
        String APELLIDOS_E=txtAPELLIDOS_E.getText();
        String TELEFONO_E=txtTELEFONO_E.getText();
        String DIRECCION_E=txtDIRECCION_E.getText();
        
            //codigo para la base de datos
        try{
                
                // se establece la conexiòn
              
                con=conectar.getConexion(dbuser, dbpassw);
                
                PreparedStatement pstm;
                pstm =(PreparedStatement)con.prepareStatement(sql);
                pstm.setInt(1,DNI_E);
                pstm.setString(2,NOMBRE_E);
                pstm.setString(3,APELLIDOS_E);
                pstm.setString(4,TELEFONO_E);
                pstm.setString(5,DIRECCION_E);
                int rtdo=pstm.executeUpdate();
                if(rtdo==1)JOptionPane.showMessageDialog(this, "Registro almacenado correctamente");
                else throw new RuntimeException("no se almaceno el registro");
                
                 con.close();
        
        }catch(RuntimeException| SQLException e){
         JOptionPane.showMessageDialog(this, "Conexion fallida");
         throw new RuntimeException(e);
                 
                 
            }
    }//GEN-LAST:event_btnGuardarActionPerformed

