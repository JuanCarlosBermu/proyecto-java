    private void btnConsultaIndActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaIndActionPerformed
        String sql;
        conexion conectar=new conexion();
        int DNI_E;
        
        try {
            con=conectar.getConexion(dbuser, dbpassw);

            DNI_E=Integer.parseInt(JOptionPane.showInputDialog
              (this,"Ingrese el DNI del empleado a consultar "));

            Statement statement = con.createStatement();
            sql=("SELECT * FROM EMPLEADO WHERE DNI_E="+DNI_E);
            ResultSet rs = statement.executeQuery(sql);
              //System.out.println("hola" + rs);
            int i=0;
            while (rs.next()) {   
                int DNI = rs.getInt("DNI_E");
                Tabla.setValueAt(DNI, i, 0);
                txtDNI_E.setText(DNI+"");
                String NOMBRE_E= rs.getString("NOMBRE_E");
                Tabla.setValueAt(NOMBRE_E, i, 1);
                txtNOMBRES_E.setText(NOMBRE_E);
                String APELLIDOS_E= rs.getString("APELLIDOS_E");
                Tabla.setValueAt(APELLIDOS_E, i, 2);
                txtAPELLIDOS_E.setText(APELLIDOS_E);
                String TELEFONO_E= rs.getString("TELEFONO_E");
                Tabla.setValueAt(TELEFONO_E, i, 3);
                txtTELEFONO_E.setText(TELEFONO_E);
                String DIRECCION_E= rs.getString("DIRECCION_E");
                Tabla.setValueAt(DIRECCION_E, i, 4);
                txtDIRECCION_E.setText(DIRECCION_E);
                ++i;
                
            }

            rs.close();
            statement.close();
            con.close();

        }     catch(RuntimeException| SQLException e){
                 e.printStackTrace();
                 JOptionPane.showMessageDialog(this, "Conexion fallida");
                 throw new RuntimeException(e);
        }
    }//GEN-LAST:event_btnConsultaIndActionPerformed

